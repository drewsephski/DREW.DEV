'use client';

import React, { useState, useEffect } from 'react';
import { motion, AnimatePresence } from 'motion/react';
import Link from 'next/link';
import { usePathname } from 'next/navigation';

interface FloatingDockItem {
  name: string;
  href: string;
  icon: React.ReactNode;
}

interface FloatingDockProps {
  items: FloatingDockItem[];
  className?: string;
}

export function FloatingDock({ items, className = '' }: FloatingDockProps) {
  const [isVisible, setIsVisible] = useState(true);
  const [lastScrollY, setLastScrollY] = useState(0);
  const pathname = usePathname();

  useEffect(() => {
    const handleScroll = () => {
      const currentScrollY = window.scrollY;

      // Show when:
      // 1. User has scrolled past the header (> 100px)
      // 2. User is scrolling up
      // 3. User has reached the bottom of the page
      const isScrollingUp = currentScrollY < lastScrollY;
      const isAtTop = currentScrollY < 100;
      const isAtBottom = window.innerHeight + currentScrollY >= document.body.offsetHeight - 100;

      if (isAtTop) {
        // Hide when at the top of the page (navbar is visible)
        setIsVisible(false);
      } else if (isAtBottom) {
        // Always show when at the bottom of the page
        setIsVisible(true);
      } else if (isScrollingUp) {
        // Show when scrolling up
        setIsVisible(true);
      } else if (currentScrollY > lastScrollY && currentScrollY > 100) {
        // Hide when scrolling down (and not at the bottom)
        setIsVisible(false);
      }

      setLastScrollY(currentScrollY);
    };

    window.addEventListener('scroll', handleScroll, { passive: true });
    return () => window.removeEventListener('scroll', handleScroll);
  }, [lastScrollY]);

  return (
    <AnimatePresence>
      {isVisible && (
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          exit={{ opacity: 0, y: 20 }}
          transition={{ duration: 0.2 }}
          className={`fixed bottom-6 left-1/2 transform -translate-x-1/2 z-50 ${className}`}
        >
          <div className="flex items-center justify-center p-2 bg-black/40 backdrop-blur-lg rounded-full border border-white/10 shadow-lg">
            <div className="flex space-x-1">
              {items.map((item, index) => {
                const isActive = pathname === item.href;

                return (
                  <Link
                    key={index}
                    href={item.href}
                    className="relative"
                  >
                    <motion.div
                      className={`relative flex items-center justify-center p-3 rounded-full transition-colors group ${
                        isActive
                          ? 'text-white bg-primary/20'
                          : 'text-white/70 hover:text-white hover:bg-white/10'
                      }`}
                      whileHover={{ scale: 1.1 }}
                      whileTap={{ scale: 0.95 }}
                    >
                      {item.icon}

                      {isActive && (
                        <motion.div
                          layoutId="activeIndicator"
                          className="absolute inset-0 bg-primary/20 rounded-full"
                          initial={false}
                          transition={{ type: 'spring', stiffness: 300, damping: 30 }}
                        />
                      )}

                      <span className="absolute -top-8 left-1/2 transform -translate-x-1/2 px-2 py-1 bg-black/80 text-white text-xs rounded opacity-0 group-hover:opacity-100 transition-opacity whitespace-nowrap">
                        {item.name}
                      </span>
                    </motion.div>
                  </Link>
                );
              })}
            </div>
          </div>
        </motion.div>
      )}
    </AnimatePresence>
  );
}

